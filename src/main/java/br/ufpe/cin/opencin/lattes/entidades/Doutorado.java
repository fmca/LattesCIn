package br.ufpe.cin.opencin.lattes.entidades;

public class Doutorado {

	private String titulo;
	private String ano;
	private String idioma;
	private String tipo_orientacao;
	private String nome_orientado;
	private String instituicao;
	private String curso;
	private String agencia_financiadora;

	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getAno() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
	public String getIdioma() {
		return idioma;
	}
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
	public String getTipo_orientacao() {
		return tipo_orientacao;
	}
	public void setTipo_orientacao(String tipo_orientacao) {
		this.tipo_orientacao = tipo_orientacao;
	}
	public String getNome_orientado() {
		return nome_orientado;
	}
	public void setNome_orientado(String nome_orientado) {
		this.nome_orientado = nome_orientado;
	}
	public String getInstituicao() {
		return instituicao;
	}
	public void setInstituicao(String instituicao) {
		this.instituicao = instituicao;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getAgencia_financiadora() {
		return agencia_financiadora;
	}
	public void setAgencia_financiadora(String agencia_financiadora) {
		this.agencia_financiadora = agencia_financiadora;
	}


}
